package com.example.myapplication.modelos;

public class ModeloCategoria {

    int id = 0;
    String titulo = "";

    public ModeloCategoria() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id){
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo){
        this.titulo = titulo;
    }

}

